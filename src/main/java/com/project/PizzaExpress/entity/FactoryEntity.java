package com.project.PizzaExpress.entity;

public class FactoryEntity {

    private String f_id;
    private String f_tel;
    private String f_addr;
    private long f_max_num;
    private long f_min_num;
    private String f_open_time;
    private String f_close_time;

    public String getF_id() {
        return f_id;
    }

    public void setF_id(String f_id) {
        this.f_id = f_id;
    }

    public String getF_tel() {
        return f_tel;
    }

    public void setF_tel(String f_tel) {
        this.f_tel = f_tel;
    }

    public String getF_addr() {
        return f_addr;
    }

    public void setF_addr(String f_addr) {
        this.f_addr = f_addr;
    }

    public long getF_max_num() {
        return f_max_num;
    }

    public void setF_max_num(long f_max_num) {
        this.f_max_num = f_max_num;
    }

    public long getF_min_num() {
        return f_min_num;
    }

    public void setF_min_num(long f_min_num) {
        this.f_min_num = f_min_num;
    }

    public String getF_open_time() {
        return f_open_time;
    }

    public void setF_open_time(String f_open_time) {
        this.f_open_time = f_open_time;
    }

    public String getF_close_time() {
        return f_close_time;
    }

    public void setF_close_time(String f_close_time) {
        this.f_close_time = f_close_time;
    }


}
